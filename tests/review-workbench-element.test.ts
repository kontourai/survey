import assert from "node:assert/strict";
import { describe, it } from "node:test";
import { execFile } from "node:child_process";
import { readFile } from "node:fs/promises";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);

describe("review-workbench-element", () => {
  it("generated CSS module is committed and annotated", async () => {
    const source = await readFile(
      "src/review-workbench/review-workbench-css.generated.ts",
      "utf8",
    );
    assert.match(
      source,
      /Generated by scripts\/copy-review-workbench-package-assets\.cjs/,
      "generated file must carry its generator annotation",
    );
    assert.match(
      source,
      /export const REVIEW_WORKBENCH_CSS/,
      "generated file must export REVIEW_WORKBENCH_CSS",
    );
    assert.match(
      source,
      /export default REVIEW_WORKBENCH_CSS/,
      "generated file must re-export default",
    );
    assert.match(
      source,
      /survey-workbench-embed/,
      "generated CSS must include the embed scope class",
    );
  });

  it("review-workbench-element imports the generated CSS module directly", async () => {
    const source = await readFile(
      "src/review-workbench/review-workbench-element.ts",
      "utf8",
    );
    assert.match(
      source,
      /from "\.\/review-workbench-css\.generated\.js"/,
      "element must import the generated CSS module statically",
    );
    assert.match(
      source,
      /REVIEW_WORKBENCH_CSS/,
      "element must use REVIEW_WORKBENCH_CSS",
    );
    // Must NOT use the old dynamic import approach
    assert.doesNotMatch(
      source,
      /review-workbench-css\.js/,
      "element must not reference the old non-generated CSS JS module",
    );
  });

  it("review-workbench-element observes src attribute", async () => {
    const source = await readFile(
      "src/review-workbench/review-workbench-element.ts",
      "utf8",
    );
    assert.match(source, /"src"/, "element must declare src in observedAttributes");
    assert.match(source, /#load\(/, "element must have a #load method for src fetching");
    assert.match(source, /fetch\(src\)/, "element must use fetch for src loading");
  });

  it("review-workbench-element renders error and empty states", async () => {
    const source = await readFile(
      "src/review-workbench/review-workbench-element.ts",
      "utf8",
    );
    assert.match(source, /#renderEmpty/, "element must have #renderEmpty method");
    assert.match(source, /#renderError/, "element must have #renderError method");
    assert.match(source, /workbench-empty/, "element must render empty state with workbench-empty class");
    assert.match(source, /workbench-error/, "element must render error state with workbench-error class");
  });

  it("review-workbench-element rescues pre-upgrade session assignment", async () => {
    const source = await readFile(
      "src/review-workbench/review-workbench-element.ts",
      "utf8",
    );
    assert.match(
      source,
      /hasOwnProperty\.call\(this, "session"\)/,
      "element must rescue pre-upgrade session assignment in connectedCallback",
    );
  });

  it("generated CSS module is in sync with CSS sources", async () => {
    // This runs the build script in --check mode, which fails if the committed
    // generated file does not match what the script would produce from source.
    await execFileAsync("node", [
      "scripts/copy-review-workbench-package-assets.cjs",
      "--check",
    ]);
  });
});
